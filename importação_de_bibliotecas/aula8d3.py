print('calculadora: seno, cosseno e tangente')
x = float(input('digite o valor do 창lgulo em graus: '))
import math
y = math.radians(x)
print('o seno deste 창ngulo vale {:.2f}' .format(math.sin(y)))
print('o cosseno deste 창ngulo vale {:.2f}' .format(math.cos(y)))
print('a tangente deste 창ngulo vale {:.2f}' .format(math.tan(y)))
